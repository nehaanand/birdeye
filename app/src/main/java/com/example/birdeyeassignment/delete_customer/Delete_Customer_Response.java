package com.example.birdeyeassignment.delete_customer;

public class Delete_Customer_Response {



    String number,firstName,middleName,emailId,phone;


    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getMiddleName() {
        return middleName;
    }

    public void setMiddleName(String middleName) {
        this.middleName = middleName;
    }

    public String getEmailId() {
        return emailId;
    }

    public void setEmailId(String emailId) {
        this.emailId = emailId;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }


    @Override
    public String toString()
    {
        return "number = "+number+ " firstName = "+firstName+ " middleName "+middleName+ " emailId = "+emailId+ " phone = "+phone;
    }
}
